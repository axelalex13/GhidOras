public class Organizator {

    private String descriere_organizator;
    private String ocupatie_organizator;
    private String nume;
    private String prenume;
    private String email;

    public String getDescriere_organizator() {
        return descriere_organizator;
    }

    public void setDescriere_organizator(String descriere_organizator) {
        this.descriere_organizator = descriere_organizator;
    }

    public String getOcupatie_organizator() {
        return ocupatie_organizator;
    }

    public void setOcupatie_organizator(String ocupatie_organizator) {
        this.ocupatie_organizator = ocupatie_organizator;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getPrenume() {
        return prenume;
    }

    public void setPrenume(String prenume) {
        this.prenume = prenume;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
